package ru.job4j.inheritance;

/**
 * Construction class.
 */
public class Construction {
    /**
     * Keeps information about ready construction.
     */
    private String constriction;

    /**
     * Setter for construction.
     *
     * @param constriction - information about ready construction.
     */

    public void setConstriction(String constriction) {
        this.constriction = constriction;
    }

    /**
     * Getter for construction.
     *
     * @return String.
     */
    public String getConstriction() {
        return constriction;
    }
}
